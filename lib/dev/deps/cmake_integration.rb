# frozen_string_literal: true

require "fileutils"
require "pathname"
require_relative "integration"
require_relative "dependency"

module Dev
  module Deps
    # CMake-specific lifecycle handler.
    #
    # install_all:
    #   1. Fetches each dep's source (git clone or tarball extract) into build/_deps/<name>-src/
    #   2. Generates deps.cmake (set variables for each dep: repo+sha or url+hash, dep lists)
    #   3. Generates deps.targets.cmake (cmake_targets, cmake_namespace per dep)
    class CmakeIntegration < Integration
      class GitCloneError < StandardError; end
      class GitCheckoutError < StandardError; end
      class DownloadError < StandardError; end
      class ExtractError < StandardError; end

      DEPS_CMAKE_FILE = "deps.cmake"
      TARGETS_CMAKE_FILE = "deps.targets.cmake"

      DEPS_CMAKE_HEADER = <<~TEXT
        # CMake dep variables: repo+sha or url+hash per runtime dep. Generated by dev - do not edit.
        # Lists below are per build target (app vs test), not per dev environment (ci/dev).
        # Edit dependencies.rb and run dev update-deps to change.

      TEXT

      TARGETS_CMAKE_HEADER = <<~TEXT
        # Runtime targets/includes per dep. Generated by dev - do not edit.
        # Used by cmake/Deps.cmake when resolving each build target (app / test).
        # Edit dependencies.rb and run dev update-deps to change.

      TEXT

      # @param repository    [Repository] source adapter for cmake deps
      # @param cache         [Cache]      shared download cache
      # @param project_root  [Pathname]   project root directory
      def initialize(repository:, cache:, project_root:)
        super(repository:, cache:)
        @project_root = Pathname(project_root)
      end

      # Install all cmake dependencies: fetch sources, generate deps.cmake
      # and deps.targets.cmake.
      #
      # @param dependencies [Array<Dependency>] cmake deps to install
      def install_all(dependencies)
        dependencies.each { |dep| fetch_dep(dep) }
        write_deps_cmake(dependencies)
        write_targets_cmake(dependencies)
      end

      private

      # Fetch a single dependency's source into build/_deps/<name>-src/.
      # Skips if the destination is already populated.
      #
      # @param dep [Dependency]
      def fetch_dep(dep)
        deps_dir = @project_root / "build" / "_deps"
        dest = deps_dir / "#{dep.name}-src"
        return if populated?(dest, dep)

        FileUtils.mkdir_p(deps_dir)

        if dep.metadata["url"]
          fetch_tarball(dep, dest)
        elsif dep.metadata["repo"]
          fetch_git(dep, dest)
        end
      end

      # Clone a git repo and check out the pinned SHA.
      #
      # @param dep  [Dependency]
      # @param dest [Pathname] destination directory
      # @raise [GitCloneError]   if git clone fails
      # @raise [GitCheckoutError] if git checkout fails
      def fetch_git(dep, dest)
        FileUtils.rm_rf(dest)
        system("git", "clone", "--no-checkout", "-q", dep.metadata["repo"], dest.to_s) ||
          raise(GitCloneError, "git clone #{dep.metadata["repo"]} failed")
        system("git", "-c", "advice.detachedHead=false", "checkout", dep.version,
               chdir: dest.to_s) ||
          raise(GitCheckoutError, "git checkout #{dep.version} failed")
      end

      # Download a URL dep's tarball (from cache if available) and extract it.
      #
      # @param dep  [Dependency]
      # @param dest [Pathname] destination directory
      # @raise [DownloadError] if curl download fails
      def fetch_tarball(dep, dest)
        cached = cache.fetch(dep.hash) if dep.hash
        if cached
          extract_tarball(cached, dest)
        else
          require "tmpdir"
          Dir.mktmpdir("dev-cmake-fetch-") do |tmpdir|
            tarball = File.join(tmpdir, "archive.tar.gz")
            system("curl", "-fsSL", "-o", tarball, dep.metadata["url"]) ||
              raise(DownloadError, "Download failed for #{dep.metadata["url"]}")
            extract_tarball(tarball, dest)
          end
        end
      end

      # Extract a tarball into the destination, expecting a single top-level directory.
      #
      # @param tarball_path [String, Pathname] path to the .tar.gz
      # @param dest         [Pathname]         extraction destination
      # @raise [ExtractError] if tar extraction fails or tarball has no top-level directory
      def extract_tarball(tarball_path, dest)
        require "tmpdir"
        FileUtils.rm_rf(dest)
        Dir.mktmpdir("dev-cmake-extract-") do |tmpdir|
          system("tar", "xzf", tarball_path.to_s, "-C", tmpdir) ||
            raise(ExtractError, "tar extract failed")
          inner = Dir.entries(tmpdir).find { |e| e != "." && e != ".." && File.directory?(File.join(tmpdir, e)) }
          raise(ExtractError, "tarball had no top-level directory") unless inner
          FileUtils.mv(File.join(tmpdir, inner), dest.to_s)
        end
      end

      # Check if a dep's source directory is already populated.
      #
      # @param dest [Pathname]   source directory to check
      # @param dep  [Dependency]
      # @return [Boolean]
      def populated?(dest, dep)
        return false unless dest.directory?

        dest.join(".git").exist? ||
          dest.join("CMakeLists.txt").exist? ||
          (dep.metadata["url"] && dest.children.any?)
      end

      # Generate deps.cmake with set() variables for each dependency.
      #
      # @param dependencies [Array<Dependency>]
      def write_deps_cmake(dependencies)
        path = @project_root / DEPS_CMAKE_FILE
        app_deps = []
        test_deps = []

        lines = [DEPS_CMAKE_HEADER]

        dependencies.each do |dep|
          if dep.metadata["url"]
            lines << "set(dep_#{dep.name}_url \"#{dep.metadata["url"]}\")\n"
            lines << "set(dep_#{dep.name}_hash \"#{dep.hash}\")\n" if dep.hash
          elsif dep.metadata["repo"]
            lines << "set(dep_#{dep.name}_repo \"#{dep.metadata["repo"]}\")\n"
            lines << "set(dep_#{dep.name}_sha \"#{dep.version}\")\n"
          end

          if dep.group == :test
            test_deps << dep.name
          else
            app_deps << dep.name
          end
        end

        lines << "set(RUNTIME_DEPS_APP \"#{app_deps.join(";")}\")\n" if app_deps.any?
        lines << "set(RUNTIME_DEPS_TEST \"#{test_deps.join(";")}\")\n" if test_deps.any?

        File.write(path, lines.join)
      end

      # Generate deps.targets.cmake with cmake_targets, cmake_namespace, and
      # includes per dependency.
      #
      # @param dependencies [Array<Dependency>]
      def write_targets_cmake(dependencies)
        path = @project_root / TARGETS_CMAKE_FILE
        lines = [TARGETS_CMAKE_HEADER]

        dependencies.each do |dep|
          meta = dep.metadata
          if meta["cmake_targets"]
            targets = Array(meta["cmake_targets"]).join(";")
            lines << "set(dep_#{dep.name}_cmake_targets \"#{targets}\")\n"
          end
          if meta["cmake_namespace"]
            lines << "set(dep_#{dep.name}_cmake_namespace \"#{meta["cmake_namespace"]}\")\n"
          end
          if meta["includes"]
            includes = Array(meta["includes"]).join(";")
            lines << "set(dep_#{dep.name}_includes \"#{includes}\")\n"
          end
        end

        File.write(path, lines.join)
      end
    end
  end
end
