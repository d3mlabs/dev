# frozen_string_literal: true

require "fileutils"
require_relative "integration"
require_relative "dependency"

module Dev
  module Deps
    # CMake-specific lifecycle handler.
    #
    # install_all:
    #   1. Fetches each dep's source (git clone or tarball extract) into build/_deps/<name>-src/
    #   2. Generates deps.cmake (set variables for each dep: repo+sha or url+hash, dep lists)
    #   3. Generates deps.targets.cmake (cmake_targets, cmake_namespace per dep)
    class CmakeIntegration < Integration
      DEPS_CMAKE = "deps.cmake"
      TARGETS_CMAKE = "deps.targets.cmake"

      LOCK_HEADER = <<~TEXT
        # Lockfile: repo+sha or url (optional hash) per runtime dep. Generated by dev deps - do not edit.
        # Lists below are per build target (app vs test), not per dev environment (ci/dev).
        # Edit dependencies.rb and re-run setup to change.
        # URL-based deps: hash is lockfile-only (reuse from lockfile or set UPDATE_DEPS_FETCH_URL_HASH=1 to fetch and pin).

      TEXT

      TARGETS_HEADER = <<~TEXT
        # Runtime targets/includes per dep. Generated by dev deps.
        # Used by cmake/Deps.cmake when resolving each build target (app / test).
        # Edit dependencies.rb and re-run setup to change.

      TEXT

      def install_all(dependencies, root:)
        dependencies.each { |dep| fetch_dep(dep, root) }
        write_deps_cmake(dependencies, root)
        write_targets_cmake(dependencies, root)
      end

      private

      def fetch_dep(dep, root)
        deps_dir = File.join(root, "build", "_deps")
        dest = File.join(deps_dir, "#{dep.name}-src")
        return if populated?(dest, dep)

        FileUtils.mkdir_p(deps_dir)

        if dep.metadata["url"]
          fetch_tarball(dep, dest)
        elsif dep.metadata["repo"]
          fetch_git(dep, dest)
        end
      end

      def fetch_git(dep, dest)
        FileUtils.rm_rf(dest)
        system("git", "clone", "--no-checkout", "-q", dep.metadata["repo"], dest) ||
          raise("git clone #{dep.metadata["repo"]} failed")
        system("git", "-c", "advice.detachedHead=false", "checkout", dep.version,
               chdir: dest) ||
          raise("git checkout #{dep.version} failed")
      end

      def fetch_tarball(dep, dest)
        cached = cache.fetch(dep.hash) if dep.hash
        if cached
          extract_tarball(cached, dest)
        else
          require "tmpdir"
          Dir.mktmpdir("dev-cmake-fetch-") do |tmpdir|
            tarball = File.join(tmpdir, "archive.tar.gz")
            system("curl", "-fsSL", "-o", tarball, dep.metadata["url"]) ||
              raise("Download failed for #{dep.metadata["url"]}")
            extract_tarball(tarball, dest)
          end
        end
      end

      def extract_tarball(tarball_path, dest)
        require "tmpdir"
        FileUtils.rm_rf(dest)
        Dir.mktmpdir("dev-cmake-extract-") do |tmpdir|
          system("tar", "xzf", tarball_path, "-C", tmpdir) ||
            raise("tar extract failed")
          inner = Dir.entries(tmpdir).find { |e| e != "." && e != ".." && File.directory?(File.join(tmpdir, e)) }
          raise "tarball had no top-level directory" unless inner
          FileUtils.mv(File.join(tmpdir, inner), dest)
        end
      end

      def populated?(dest, dep)
        return false unless File.directory?(dest)
        File.exist?(File.join(dest, ".git")) ||
          File.exist?(File.join(dest, "CMakeLists.txt")) ||
          (dep.metadata["url"] && !Dir.empty?(dest))
      end

      def write_deps_cmake(dependencies, root)
        path = File.join(root, DEPS_CMAKE)
        app_deps = []
        test_deps = []

        lines = [LOCK_HEADER]

        dependencies.each do |dep|
          if dep.metadata["url"]
            lines << "set(dep_#{dep.name}_url \"#{dep.metadata["url"]}\")\n"
            lines << "set(dep_#{dep.name}_hash \"#{dep.hash}\")\n" if dep.hash
          elsif dep.metadata["repo"]
            lines << "set(dep_#{dep.name}_repo \"#{dep.metadata["repo"]}\")\n"
            lines << "set(dep_#{dep.name}_sha \"#{dep.version}\")\n"
          end

          if dep.group == :test
            test_deps << dep.name
          else
            app_deps << dep.name
          end
        end

        lines << "set(RUNTIME_DEPS_APP \"#{app_deps.join(";")}\")\n" if app_deps.any?
        lines << "set(RUNTIME_DEPS_TEST \"#{test_deps.join(";")}\")\n" if test_deps.any?

        File.write(path, lines.join)
      end

      def write_targets_cmake(dependencies, root)
        path = File.join(root, TARGETS_CMAKE)
        lines = [TARGETS_HEADER]

        dependencies.each do |dep|
          meta = dep.metadata
          if meta["cmake_targets"]
            targets = Array(meta["cmake_targets"]).join(";")
            lines << "set(dep_#{dep.name}_cmake_targets \"#{targets}\")\n"
          end
          if meta["cmake_namespace"]
            lines << "set(dep_#{dep.name}_cmake_namespace \"#{meta["cmake_namespace"]}\")\n"
          end
          if meta["includes"]
            includes = Array(meta["includes"]).join(";")
            lines << "set(dep_#{dep.name}_includes \"#{includes}\")\n"
          end
        end

        File.write(path, lines.join)
      end
    end
  end
end
