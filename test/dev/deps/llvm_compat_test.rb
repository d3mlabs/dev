# typed: false
# frozen_string_literal: true

require "test_helper"
require "shadowenv_llvm"
require "tmpdir"
require "yaml"

transform!(RSpock::AST::Transformation)
class LlvmCompatTest < Minitest::Test
  test "project_needs_llvm? detects llvm in YAML build-deps.lock" do
    Given "a YAML lockfile containing llvm"
    dir = Dir.mktmpdir("dev-llvm-compat-")
    yaml_content = {
      "llvm" => {
        "integration" => "brew",
        "group" => "build",
        "version" => "22.1.0",
        "hash" => "SHA256=abc",
      },
    }
    File.write(File.join(dir, "build-deps.lock"), "# Generated by dev. Do not edit.\n" + YAML.dump(yaml_content))

    When "checking for llvm"
    result = ShadowenvLlvm.project_needs_llvm?(Pathname(dir))

    Then
    result == true

    Cleanup
    FileUtils.rm_rf(dir)
  end

  test "project_needs_llvm? returns false when no llvm in build-deps.lock" do
    Given "a YAML lockfile without llvm"
    dir = Dir.mktmpdir("dev-llvm-compat-")
    yaml_content = {
      "cmake" => {
        "integration" => "brew",
        "group" => "build",
        "version" => "3.31.4",
      },
    }
    File.write(File.join(dir, "build-deps.lock"), YAML.dump(yaml_content))

    When "checking for llvm"
    result = ShadowenvLlvm.project_needs_llvm?(Pathname(dir))

    Then
    result == false

    Cleanup
    FileUtils.rm_rf(dir)
  end

  test "project_needs_llvm? still works with old plain-text format" do
    Given "a plain-text lockfile with brew llvm"
    dir = Dir.mktmpdir("dev-llvm-compat-")
    File.write(File.join(dir, "build-deps.lock"), "brew llvm\nbrew cmake\n")

    When "checking for llvm"
    result = ShadowenvLlvm.project_needs_llvm?(Pathname(dir))

    Then
    result == true

    Cleanup
    FileUtils.rm_rf(dir)
  end

  test "project_needs_llvm? returns false when no build-deps.lock exists" do
    Given "a directory with no lockfile"
    dir = Dir.mktmpdir("dev-llvm-compat-")

    When "checking for llvm"
    result = ShadowenvLlvm.project_needs_llvm?(Pathname(dir))

    Then
    result == false

    Cleanup
    FileUtils.rm_rf(dir)
  end
end
