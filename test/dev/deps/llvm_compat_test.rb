# typed: false
# frozen_string_literal: true

require "test_helper"
require "dev/command_runner"
require "tmpdir"
require "yaml"

transform!(RSpock::AST::Transformation)
class LlvmCompatTest < Minitest::Test
  extend T::Sig
  include SorbetHelper

  test "project_needs_llvm? detects llvm in YAML build-deps.lock" do
    Given
    dir = Dir.mktmpdir("dev-llvm-compat-")
    lockfile = File.join(dir, "build-deps.lock")
    yaml_content = {
      "llvm" => {
        "integration" => "brew",
        "group" => "build",
        "version" => "22.1.0",
        "hash" => "SHA256=abc",
      },
    }
    File.write(lockfile, "# Generated by dev. Do not edit.\n" + YAML.dump(yaml_content))

    ui = typed_mock(Dev::Cli::Ui)
    runner = Dev::CommandRunner.new(ui: ui, ruby_version: "4.0.1")

    When
    result = runner.send(:project_needs_llvm?, Pathname.new(dir))

    Then
    result == true

    Cleanup
    FileUtils.rm_rf(dir)
  end

  test "project_needs_llvm? returns false when no llvm in build-deps.lock" do
    Given
    dir = Dir.mktmpdir("dev-llvm-compat-")
    lockfile = File.join(dir, "build-deps.lock")
    yaml_content = {
      "cmake" => {
        "integration" => "brew",
        "group" => "build",
        "version" => "3.31.4",
      },
    }
    File.write(lockfile, YAML.dump(yaml_content))

    ui = typed_mock(Dev::Cli::Ui)
    runner = Dev::CommandRunner.new(ui: ui, ruby_version: "4.0.1")

    When
    result = runner.send(:project_needs_llvm?, Pathname.new(dir))

    Then
    result == false

    Cleanup
    FileUtils.rm_rf(dir)
  end

  test "project_needs_llvm? still works with old plain-text format" do
    Given
    dir = Dir.mktmpdir("dev-llvm-compat-")
    lockfile = File.join(dir, "build-deps.lock")
    File.write(lockfile, "brew llvm\nbrew cmake\n")

    ui = typed_mock(Dev::Cli::Ui)
    runner = Dev::CommandRunner.new(ui: ui, ruby_version: "4.0.1")

    When
    result = runner.send(:project_needs_llvm?, Pathname.new(dir))

    Then
    result == true

    Cleanup
    FileUtils.rm_rf(dir)
  end
end
